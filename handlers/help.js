'use strict';

const OWNER_ID = parseInt(process.env.OWNER_ID);

async function handleHelp(ctx) {
  const uid  = ctx.from?.id;
  const isOwner = uid === OWNER_ID;
  const isAdmin = ctx.isAdmin || ctx.isOwner;
  const isGroup = ['group','supergroup'].includes(ctx.chat?.type);

  // ── مستخدم عادي (خاص أو قروب) ──
  const userHelp =
`📚 *أوامر المستخدمين*
━━━━━━━━━━━━━━━━━━━━

🔍 *البحث والمحتوى*
\`+ اسم الملف\` — بحث سريع في القروب
\`/start\` — القائمة الرئيسية

🎮 *الألعاب*
\`مليون\` — بدء لعبة من سيربح المليون
\`خمن\` — تحدي خمن الصورة
\`انا\` — الانضمام للعبة جارية

💰 *البنك*
\`انشاء حساب\` — فتح حساب بنكي
\`فلوسي\` — عرض رصيدك
\`فارسي [مبلغ]\` — تحويل لشخص (رد على رسالته)
\`rip [مبلغ]\` — إقراض شخص (رد على رسالته)
\`/daily\` أو \`يومي\` — مكافأة يومية
\`/flip [مبلغ]\` أو \`عملة\` — قلب عملة
\`/rob\` أو \`سرقة\` — سرقة (رد على رسالة)
\`/leaderboard\` أو \`متصدرين\` — أثرى اللاعبين
\`/market\` أو \`متجر\` — متجر البوت

🎯 *تسلية*
\`/truth\` أو \`صح\` — سؤال صراحة
\`/dare\` أو \`جرأة\` — تحدي جرأة
\`/couple\` أو \`زوج\` — زوج اليوم
\`/slot\` أو \`سلوت\` — ماكينة القمار

ℹ️ *معلومات*
\`/info [@مستخدم]\` — معلومات عضو
\`/stats\` — إحصائيات القروب
\`/rules\` أو \`قواعد\` — قواعد القروب
\`/gamestatus\` — حالة الألعاب الجارية`;

  // ── مشرف القروب ──
  const adminHelp =
`\n\n👮 *أوامر المشرفين* _(في القروب فقط)_
━━━━━━━━━━━━━━━━━━━━

🛡️ *الإدارة*
\`/ban [@مستخدم] [سبب]\` — حظر عضو
\`/unban [@مستخدم]\` — رفع الحظر
\`/kick [@مستخدم]\` — طرد عضو
\`/mute [@مستخدم] [مدة]\` — إسكات (10m/1h/1d)
\`/unmute [@مستخدم]\` — رفع الإسكات
\`/warn [@مستخدم] [سبب]\` — تحذير
\`/unwarn [@مستخدم]\` — مسح تحذير
\`/warns [@مستخدم]\` — عرض التحذيرات
\`/promote [@مستخدم]\` — ترقية لمشرف
\`/demote [@مستخدم]\` — إزالة صلاحيات

📌 *الرسائل*
\`/pin\` — تثبيت رسالة (رد عليها)
\`/unpin\` — إلغاء التثبيت
\`/del\` أو \`حذف\` — حذف رسالة (رد عليها)
\`/purge [عدد]\` أو \`مسح\` — حذف رسائل متعددة
\`/clean\` — تنظيف رسائل البوت

👥 *الأعضاء*
\`/all\` — عرض كل الأعضاء
\`/tag\` — تاق الجميع
\`العاب\` — قائمة الألعاب

⚙️ *الإعدادات*
\`/setlog [@قناة]\` أو \`سجل\` — تعيين قناة السجل
\`/adminhelp\` — مساعدة المشرفين
\`/mygroups\` — إدارة قروباتك`;

  // ── المالك ──
  const ownerHelp =
`\n\n👑 *أوامر المالك*
━━━━━━━━━━━━━━━━━━━━

\`/mstop\` — إيقاف لعبة المليون
\`/mtop\` — لوحة متصدري المليون
\`/maddq\` — إضافة سؤال للمليون
\`/mstats\` — إحصائيات المليون
\`/summarize\` أو \`لخص\` — تلخيص ملف
\`/cmds\` أو \`اوامر\` — قائمة الأوامر`;

  let text = userHelp;
  if (isAdmin || isGroup) text += adminHelp;
  if (isOwner) text += ownerHelp;

  text += '\n\n━━━━━━━━━━━━━━━━━━━━\n💡 _الأوامر تعمل بالعربي والإنجليزي_';

  return ctx.reply(text, {
    parse_mode: 'Markdown',
    reply_to_message_id: ctx.message?.message_id,
    disable_web_page_preview: true,
  }).catch(() => {});
}

module.exports = { handleHelp };
